object NicknameGenerator {
    fun generate(): String {
        val restaurant = restaurants.random()
        val menu = menus.random()
        val uuidPart = UUID.randomUUID().toString().substring(0, 4) // 닉네임 30자 제한
        return "${restaurant}_${menu}_$uuidPart"
    }

    fun generateBatch(count: Int): List<String> {
        return List(count) { generate() }
    }
}
